class LandingPageSteps {
	/**
	 * The step definitions below match with Katalon sample Gherkin steps
	 */
	@Given("I navigate to WhatsApp Landing Page")
	def i_navigate_to_whatsapp_landing_page() {
		Windows.startApplication('C:\\Users\\Tia\\AppData\\Local\\WhatsApp\\app-2.2202.12\\WhatsApp.exe')
	}

	@When("I see there is a (.*) in this page")
	def I_see_there_is_a_value_in_this_page(String value) {
		String text = Windows.getText(findWindowsObject('Object Repository/LandingPage/TextToUse_landingPage'))
		assert text == value
	}

	@Then("I verify this is a Landing page")
	def I_verify_this_is_a_landing_page() {
		Windows.verifyElementPresent(findWindowsObject('Object Repository/LandingPage/ImageQRCode'), 1)
		Windows.closeApplication()
	}
}